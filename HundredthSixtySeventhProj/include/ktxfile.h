#ifndef _KTXFILE_H_
#define _KTXFILE_H_

#include <stdlib.h>

struct ktx_image {
    void* data;
    size_t data_sz;
    unsigned int width;
    unsigned int height;
    unsigned short channels;
    unsigned short bit_depth;
    unsigned short compression_type;
    struct {
        int compressed : 1;
        int hdr : 1;
    } flags;
};

int ktx_image_read(struct ktx_image* ktx, const void* fdata);
void ktx_image_free(struct ktx_image* ktx);

#endif /* ! _KTXFILE_H_ */
