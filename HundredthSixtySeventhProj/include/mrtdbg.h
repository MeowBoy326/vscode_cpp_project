#ifndef _MRTDBG_H_
#define _MRTDBG_H_

struct mrtdbg_tex_info {
    /* Texture OpenGL id */
    unsigned int handle;
    /* 0 for simple texture, 1 for texture array */
    unsigned int type;
    /* Layer to use (when type is 1) */
    unsigned int layer;
    /* Mode of operation */
    enum {
        MRTDBG_MODE_RGB = 0,
        MRTDBG_MODE_MONO_R = 1,
        MRTDBG_MODE_MONO_G = 2,
        MRTDBG_MODE_MONO_B = 3,
        MRTDBG_MODE_MONO_A = 4
    } mode;
};

void mrtdbg_init();
void mrtdbg_show_fbo_textures(unsigned int fbo);
void mrtdbg_show_textures(struct mrtdbg_tex_info* textures, unsigned int num_textures);
void mrtdbg_destroy();

#endif /* ! _MRTDBG_H_ */
